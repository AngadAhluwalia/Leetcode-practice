--TASK 1 TABLE CREATION
CREATE TABLE JOB(
JOB_CODE INT PRIMARY KEY,
JOB_DESCRIPTION VARCHAR(50) NOT NULL,
JOB_CHG_HOUR NUMERIC(6,2) NOT NULL,
JOB_LAST_UPDATE DATE NOT NULL
);
CREATE TABLE EMPLOYEE(
EMP_NUM INT PRIMARY KEY,
EMP_LNAME VARCHAR(30) NOT NULL,
EMP_FNAME VARCHAR(30) NOT NULL,
EMP_INITIAL CHAR(1) NULL,
EMP_HIREDATE DATE NOT NULL,
JOB_CODE INT NOT NULL,

FOREIGN KEY (JOB_CODE) 
REFERENCES JOB(JOB_CODE)
);
CREATE TABLE PROJECT(
PROJ_NUM INT PRIMARY KEY,
PROJ_NAME VARCHAR(50) NOT NULL,
PROJ_VALUE NUMERIC(12,2) NOT NULL,
PROJ_BALANCE NUMERIC(12,2) NOT NULL,
EMP_NUM INT NOT NULL,

FOREIGN KEY (EMP_NUM)
REFERENCES EMPLOYEE(EMP_NUM)
);
CREATE TABLE ASSIGNMENT(
ASSIGN_NUM INT PRIMARY KEY,
ASSIGN_DATE DATE NOT NULL,
PROJ_NUM INT NOT NULL,
EMP_NUM INT NOT NULL,
ASSIGN_JOB INT NOT NULL,
ASSIGN_CHG_HR NUMERIC(6,2) NOT NULL,
ASSIGN_HOURS NUMERIC(5,1) NOT NULL,
ASSIGN_CHARGE NUMERIC(8,2) NOT NULL,

FOREIGN KEY (PROJ_NUM)
REFERENCES PROJECT(PROJ_NUM),

FOREIGN KEY (EMP_NUM)
REFERENCES EMPLOYEE(EMP_NUM),

FOREIGN KEY (ASSIGN_JOB)
REFERENCES JOB(JOB_CODE)
);

