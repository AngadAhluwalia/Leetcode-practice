SELECT 
EMPLOYEE.EMP_NUM,
EMPLOYEE.EMP_LNAME,
EMPLOYEE.EMP_FNAME,

COUNT(DISTINCT ASSIGNMENT.PROJ_NUM) AS NUM_PROJECTS

