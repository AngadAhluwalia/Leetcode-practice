JOIN ASSIGNMENT
ON EMPLOYEE.EMP_NUM = ASSIGNMENT.EMP_NUM

GROUP BY 
EMPLOYEE.EMP_NUM,
EMPLOYEE.EMP_LNAME,
EMPLOYEE.EMP_FNAME

HAVING COUNT(DISTINCT ASSIGNMENT.PROJ_NUM) > 1;
