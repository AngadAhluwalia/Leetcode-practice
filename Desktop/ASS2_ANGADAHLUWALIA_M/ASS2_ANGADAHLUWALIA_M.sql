EMPLOYEE.EMP_FNAME,
EMPLOYEE.EMP_LNAME

FROM EMPLOYEE
JOIN ASSIGNMENT 
ON EMPLOYEE.EMP_NUM = ASSIGNMENT.EMP_NUM

JOIN PROJECT
ON ASSIGNMENT.PROJ_NUM = PROJECT.PROJ_NUM

WHERE PROJECT.PROJ_NAME = 'Evergreen';

-- Query 4
SELECT 
PROJECT.PROJ_NAME,

COUNT(DISTINCT ASSIGNMENT.EMP_NUM) AS NUM_EMPLOYEES,

SUM(ASSIGNMENT.ASSIGN_HOURS) AS TOTAL_HOURS,

SUM(ASSIGNMENT.ASSIGN_CHARGE) AS TOTAL_CHARGES

FROM PROJECT

JOIN ASSIGNMENT
ON PROJECT.PROJ_NUM = ASSIGNMENT.PROJ_NUM

GROUP BY PROJECT.PROJ_NAME

ORDER BY PROJECT.PROJ_NAME;

